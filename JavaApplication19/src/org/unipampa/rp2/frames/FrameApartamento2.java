    /**
     * Recebe o codigo e adiciona a lista a monografia selecionada
     *
     * @param cod
     */
    public void listarCod(int cod) {
        DefaultListModel listModel = new DefaultListModel();
        if (cod == -1) {
            List<Imovel> list = listaApartamento.getLista();
            for (Imovel casa : list) {
                listModel.addElement(casa.toString());
            }
            jListFrameCasa.setModel(listModel);
        } else {
            Imovel casa = listaApartamento.consultar(cod);
            if (casa == null) {
                JOptionPane.showMessageDialog(null, "Não encontrado");
            } else {
                listModel.addElement(casa.toString());
            }

            jListFrameCasa.setModel(listModel);//Mudar a lista
        }
    }

    public void listarValor(double valor) {
        DefaultListModel listModel = new DefaultListModel();
        List<Imovel> casa = listaApartamento.pesquisaValor(valor);

        if (casa == null) {
            JOptionPane.showMessageDialog(null, "Não encontrado");

        } else {
            for (Imovel home : casa) {
                listModel.addElement(home.toString());
            }
        }

        jListFrameCasa.setModel(listModel);
    }

    public void listarBairro(String bairro) {
        DefaultListModel listModel = new DefaultListModel();
        List<Imovel> apartamnto = listaApartamento.pesquisaBairro(bairro);

        if (apartamnto == null) {
            JOptionPane.showMessageDialog(null, "Não encontrado");
        } else {
            for (Imovel home : apartamnto) {
                listModel.addElement(home.toString());
            }
        }

        jListFrameCasa.setModel(listModel);
    }

    public void soNumeros(java.awt.event.KeyEvent evt) {
        char aux = evt.getKeyChar();
        if (!Character.isDigit(aux)) {
            evt.consume();
        }
    }

    public void soDouble(java.awt.event.KeyEvent evt, String text) {
        char aux = evt.getKeyChar();
        int tem = 0;
        for (int i = 0; i < text.length(); i++) {
            if (text.charAt(i) == '.') {
                tem++;
                break;
            }
        }

        if (aux == '.') {
            if (tem != 0) {
                evt.consume();
            }
        } else if (!Character.isDigit(aux)) {
            evt.consume();
        }
    }

    /**
     * Retorna true caso consiga preencher os campos com o objeto do codigo
     * selecionado e false caso contrário
     *
     * @param cod
     * @return
     */
    public boolean preencherCampos(int cod) {

        Apartamento a = (Apartamento) listaApartamento.consultar(cod);
        if (a != null) {
            jTextFieldAndar.setText(String.valueOf(a.getAndar()));
            jTextFieldAnoC.setText(String.valueOf(a.getAnoConstrucao()));
            jTextFieldAreaTotal.setText(String.valueOf(a.getAreaTotal()));
            jTextFieldBairro.setText(a.getBairro());
            jTextFieldDescricao.setText(a.getDescricao());
            jTextFieldLogradouro.setText(a.getLogradouro());
            jTextFieldNApartamento.setText(String.valueOf(a.getNroApartamento()));
            jTextFieldNQuartos.setText(String.valueOf(a.getNroQuartos()));
            jTextFieldNVGaragem.setText(String.valueOf(a.getVgsGaragem()));
            jTextFieldNomeEdificio.setText(a.getNomeEdificio());
            jTextFieldValorCondominio.setText(String.valueOf(a.getValorCondominio()));
            jTextFieldCidade.setText(a.getCidade());
            jTextFieldNumero.setText(String.valueOf(a.getNumero()));
            return true;
        } else {
            JOptionPane.showMessageDialog(null, "ERRO 03 - Imóvel não encontrado");
            return false;
        }
    }


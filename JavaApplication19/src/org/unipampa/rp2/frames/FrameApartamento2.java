    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (jTextFieldAndar.getText().equals("")
                || jTextFieldAnoC.getText().equals("")
                || jTextFieldAreaTotal.getText().equals("")
                || jTextFieldBairro.getText().equals("")
                || jTextFieldDescricao.getText().equals("")
                || jTextFieldLogradouro.getText().equals("")
                || jTextFieldNApartamento.getText().equals("")
                || jTextFieldNQuartos.getText().equals("")
                || jTextFieldNVGaragem.getText().equals("")
                || jTextFieldNomeEdificio.getText().equals("")
                || jTextFieldValorCondominio.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");

        } else if (isEditar) {
            Apartamento p = new Apartamento(jTextFieldCidade.getText(), jTextFieldBairro.getText(),
                    jTextFieldLogradouro.getText(), Integer.parseInt(jTextFieldNumero.getText()),
                    Double.parseDouble(jTextFieldValorCondominio.getText()), jTextFieldNomeEdificio.getText(),
                    Integer.parseInt(jTextFieldAndar.getText()), Integer.parseInt(jTextFieldNApartamento.getText()),
                    Integer.parseInt(jTextFieldAnoC.getText()), Integer.parseInt(jTextFieldNQuartos.getText()),
                    Integer.parseInt(jTextFieldNVGaragem.getText()), Double.parseDouble(jTextFieldValorCondominio.getText()),
                    jTextFieldDescricao.getText(), Double.parseDouble(jTextFieldAreaTotal.getText()));

            if (listaApartamento.editar(isCod, p)) {
                jTabbedPaneCasa.setEnabledAt(1, false);
                jTabbedPaneCasa.setEnabledAt(0, true);
                jTabbedPaneCasa.setSelectedIndex(0);
                try {
                    listaApartamento.gravarUltimoCod(isCod);
                } catch (IOException ex) {
                    Logger.getLogger(FrameApartamento2.class.getName()).log(Level.SEVERE, null, ex);
                }
                listaApartamento.escreverArquivo();
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao Editar");
            }
        } else {
            Apartamento p = new Apartamento(jTextFieldCidade.getText(), jTextFieldBairro.getText(),
                    jTextFieldLogradouro.getText(), Integer.parseInt(jTextFieldNumero.getText()),
                    Double.parseDouble(jTextFieldValorCondominio.getText()), jTextFieldNomeEdificio.getText(),
                    Integer.parseInt(jTextFieldAndar.getText()), Integer.parseInt(jTextFieldNApartamento.getText()),
                    Integer.parseInt(jTextFieldAnoC.getText()), Integer.parseInt(jTextFieldNQuartos.getText()),
                    Integer.parseInt(jTextFieldNVGaragem.getText()), Double.parseDouble(jTextFieldValorCondominio.getText()),
                    jTextFieldDescricao.getText(), Double.parseDouble(jTextFieldAreaTotal.getText()));

            if (listaApartamento.incluir(p)) {
                jTabbedPaneCasa.setEnabledAt(1, false);
                jTabbedPaneCasa.setEnabledAt(0, true);
                jTabbedPaneCasa.setSelectedIndex(0);
                try {
                    this.listaApartamento.gravarUltimoCod(p.getCod());
                } catch (IOException ex) {
                    Logger.getLogger(FrameApartamento2.class.getName()).log(Level.SEVERE, null, ex);
                }
                listaApartamento.escreverArquivo();
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao Incluir");
            }

        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

